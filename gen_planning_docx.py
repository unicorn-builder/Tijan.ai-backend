def _t(key, lang="fr"):
    return _T.get(lang, _T["fr"]).get(key, key)


def _fmt_fcfa(v):
    """Format value as FCFA currency."""
    if not v or v == 0:
        return '—'
    if v >= 1e9:
        return f"{v/1e9:.2f} Mds FCFA"
    if v >= 1e6:
        return f"{v/1e6:.1f} M FCFA"
    return f"{int(v):,} FCFA".replace(',', ' ')


def _set_cell_background(cell, color_hex):
    """Set cell background color."""
    shading = cell._element.get_or_add_tcPr()
    shading_elm = OxmlElement('w:shd')
    shading_elm.set(qn('w:fill'), color_hex)
    shading.append(shading_elm)


def _add_styled_table(doc, headers, rows, col_widths=None, header_color='43A956', zebra=False):
    """Add a professionally styled table with header row shading."""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    if col_widths:
        for i, w in enumerate(col_widths):
            for row in table.rows:
                row.cells[i].width = Cm(w)

    # Header row
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = str(h)
        p = cell.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.runs[0]
        run.bold = True
        run.font.size = Pt(9)
        run.font.color.rgb = BLANC
        run.font.name = 'Calibri'
        _set_cell_background(cell, header_color)

    # Data rows
    for r_idx, row_data in enumerate(rows):
        row = table.rows[r_idx + 1]
        for c_idx, val in enumerate(row_data):
            cell = row.cells[c_idx]
            cell.text = str(val)
            p = cell.paragraphs[0]
            run = p.runs[0] if p.runs else p.add_run(str(val))
            run.font.size = Pt(8)
            run.font.name = 'Calibri'
            if zebra and r_idx % 2 == 0:
                _set_cell_background(cell, 'F5F5F5')

    return table


def _add_title(doc, text, lang='fr'):
    """Add a green Tijan-style title."""
    title = doc.add_heading(text, level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in title.runs:
        run.font.color.rgb = VERT
        run.font.size = Pt(18)
        run.font.name = 'Calibri'


def _add_footer(doc, lang='fr'):
    """Add a Tijan footer."""
    footer_p = doc.add_paragraph(f'\n\n{_t("generated_by", lang)}')
    footer_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in footer_p.runs:
        run.font.size = Pt(8)
        run.font.italic = True
        run.font.color.rgb = GRIS
        run.font.name = 'Calibri'


