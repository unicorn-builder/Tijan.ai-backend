def generer_tresorerie_docx(planning, lang='fr') -> bytes:
    """
    Generate Plan de Tresorerie as Word (.docx).

    Args:
        planning: Planning object from engine_planning.py
        lang: 'fr' or 'en'

    Returns:
        bytes: DOCX document content
    """
    doc = Document()

    # Set default font
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Calibri'
    font.size = Pt(10)

    # ── TITLE ──
    _add_title(doc, _t("title_tresorerie", lang), lang)

    # ── PROJECT INFO ──
    doc.add_heading(_t("project_info", lang), level=1)

    info_data = [
        [_t("nom", lang), planning.projet_nom],
        [_t("ville", lang), planning.ville],
        [_t("surface", lang), f'{int(planning.surface_batie_m2):,} m²'.replace(',', ' ')],
        [_t("duree", lang), f'{planning.duree_totale_mois} {_t("mois", lang)}'],
    ]
    _add_styled_table(doc, [_t("project_info", lang), ''], info_data,
                      col_widths=[5.0, 12.0], header_color='43A956')

    doc.add_paragraph()

    # ── MONTHLY CASH FLOW TABLE ──
    doc.add_page_break()
    doc.add_heading(_t("tresorerie_mensuelle", lang), level=1)

    monthly_headers = [
        _t("col_mois", lang),
        _t("col_materiaux", lang),
        _t("col_mo", lang),
        _t("col_total_mensuel", lang),
        _t("col_cumul", lang),
    ]

    monthly_rows = []
    for m in planning.tresorerie_mensuelle:
        monthly_rows.append([
            str(m.get('mois', '')),
            _fmt_fcfa(m.get('depense_materiel_fcfa', 0)),
            _fmt_fcfa(m.get('depense_pose_fcfa', 0)),
            _fmt_fcfa(m.get('depense_total_fcfa', 0)),
            _fmt_fcfa(m.get('cumul_fcfa', 0)),
        ])

    # Add total row
    if planning.tresorerie_mensuelle:
        total_mat = sum(m.get('depense_materiel_fcfa', 0) for m in planning.tresorerie_mensuelle)
        total_pose = sum(m.get('depense_pose_fcfa', 0) for m in planning.tresorerie_mensuelle)
        monthly_rows.append([
            _t("total", lang),
            _fmt_fcfa(total_mat),
            _fmt_fcfa(total_pose),
            _fmt_fcfa(planning.cout_total_fcfa),
            _fmt_fcfa(planning.cout_total_fcfa),
        ])

    table = _add_styled_table(doc, monthly_headers, monthly_rows,
                              col_widths=[2.5, 3.5, 3.5, 3.5, 3.5],
                              header_color='43A956', zebra=True)

    # Bold the total row
    if monthly_rows:
        last_row = table.rows[-1]
        for cell in last_row.cells:
            _set_cell_background(cell, 'E8F5EA')
            for p in cell.paragraphs:
                for run in p.runs:
                    run.bold = True

    doc.add_paragraph()

    # ── LOT × PHASE CROSS-TAB ──
    doc.add_page_break()
    doc.add_heading(_t("tresorerie_lot_phase", lang), level=1)

    lp_headers = [
        _t("col_lot", lang),
        _t("col_phase", lang),
        _t("col_cout", lang),
    ]

    lp_rows = []
    total_lp = 0
    for item in planning.tresorerie_lot_phase:
        cout = item.get('cout_total_fcfa', 0)
        total_lp += cout
        lp_rows.append([
            str(item.get('lot', '')),
            str(item.get('phase', '')),
            _fmt_fcfa(cout),
        ])

    # Add total row
    lp_rows.append([
        _t("total", lang),
        '',
        _fmt_fcfa(total_lp),
    ])

    table_lp = _add_styled_table(doc, lp_headers, lp_rows,
                                 col_widths=[5.0, 6.0, 5.0],
                                 header_color='43A956', zebra=True)

    # Bold the total row
    if lp_rows:
        last_row = table_lp.rows[-1]
        for cell in last_row.cells:
            _set_cell_background(cell, 'E8F5EA')
            for p in cell.paragraphs:
                for run in p.runs:
                    run.bold = True

    doc.add_paragraph()

    # ── FOOTER ──
    _add_footer(doc, lang)

    # Save to bytes
    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()
