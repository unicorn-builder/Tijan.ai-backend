def generer_planning_docx(planning, lang='fr') -> bytes:
    """
    Generate Planning d'execution as Word (.docx).

    Args:
        planning: Planning object from engine_planning.py
        lang: 'fr' or 'en'

    Returns:
        bytes: DOCX document content
    """
    doc = Document()

    # Set default font
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Calibri'
    font.size = Pt(10)

    # ── TITLE ──
    _add_title(doc, _t("title_planning", lang), lang)

    # ── PROJECT INFO ──
    doc.add_heading(_t("project_info", lang), level=1)

    info_data = [
        [_t("nom", lang), planning.projet_nom],
        [_t("ville", lang), planning.ville],
        [_t("niveaux", lang), f'R+{planning.nb_niveaux - 1} ({planning.nb_niveaux} {_t("niveaux", lang).lower()})'],
        [_t("surface", lang), f'{int(planning.surface_batie_m2):,} m²'.replace(',', ' ')],
        [_t("duree", lang), f'{planning.duree_totale_jours} {_t("jours", lang)} ({planning.duree_totale_mois} {_t("mois", lang)})'],
    ]
    _add_styled_table(doc, [_t("project_info", lang), ''], info_data,
                      col_widths=[5.0, 12.0], header_color='43A956')

    doc.add_paragraph()

    # ── TASK LIST BY LOT ──
    doc.add_page_break()
    doc.add_heading(_t("section_tasks", lang), level=1)

    # Group tasks by lot
    lots = {}
    for tache in planning.taches:
        lot_key = tache.lot
        if lot_key not in lots:
            lots[lot_key] = []
        lots[lot_key].append(tache)

    lot_labels = {
        "STRUCTURE": _t("lot_structure", lang),
        "MEP": _t("lot_mep", lang),
        "FINITIONS": _t("lot_finitions", lang),
    }

    task_headers = [
        _t("col_no", lang),
        _t("col_tache", lang),
        _t("col_duree", lang),
        _t("col_debut", lang),
        _t("col_fin", lang),
    ]

    for lot_key in ["STRUCTURE", "MEP", "FINITIONS"]:
        taches = lots.get(lot_key, [])
        if not taches:
            continue

        lot_label = lot_labels.get(lot_key, lot_key)
        doc.add_heading(lot_label, level=2)

        task_rows = []
        for i, tache in enumerate(taches):
            task_rows.append([
                tache.id,
                tache.designation,
                str(tache.duree_jours),
                str(tache.debut_jour),
                str(tache.fin_jour),
            ])

        _add_styled_table(doc, task_headers, task_rows,
                          col_widths=[2.0, 8.0, 2.5, 2.5, 2.5],
                          header_color='43A956', zebra=True)

        doc.add_paragraph()

    # ── FOOTER ──
    _add_footer(doc, lang)

    # Save to bytes
    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


