def _boq_structure_items(rs, params):
    """Build structure BOQ items: list of (no, desig, qty, unit, obs)."""
    d = rs.params
    boq = rs.boq
    fond = rs.fondation
    dalle = rs.dalle
    poutre = rs.poutre_principale
    poteaux = rs.poteaux
    cl = rs.cloisons
    nb_pot = (d.nb_travees_x + 1) * (d.nb_travees_y + 1)
    ep_dalle_m = dalle.epaisseur_mm / 1000
    surf_batie = boq.surface_batie_m2
    perim = 4 * math.sqrt(d.surface_emprise_m2)

    items = []

    # Installation
    items.append(("1.1", "Cloture de chantier", int(perim), "ml", ""))
    items.append(("1.2", "Base vie chantier", 1, "fft", "Modulaires"))
    items.append(("1.3", "Branchements provisoires", 1, "fft", ""))

    # Terrassement
    V_fouilles = d.surface_emprise_m2 * (fond.profondeur_m + 0.50)
    items.append(("2.1", "Decapage terre vegetale e=30cm", int(d.surface_emprise_m2 * 0.30), "m3", ""))
    items.append(("2.2", "Fouilles generales mecaniques", int(V_fouilles), "m3", ""))
    items.append(("2.3", "Remblai compacte", int(V_fouilles * 0.30), "m3", ""))
    items.append(("2.4", "Evacuation terres", int(V_fouilles * 0.70), "m3", ""))

    # Fondations
    if fond.nb_pieux > 0:
        items.append(("3.1", f"Pieux fores D{fond.diam_pieu_mm}mm L={fond.longueur_pieu_m}m",
                       int(fond.nb_pieux * fond.longueur_pieu_m), "ml", f"{fond.nb_pieux} pieux"))
        items.append(("3.2", f"Armatures pieux {rs.classe_acier}",
                       int(fond.nb_pieux * fond.As_cm2 / 10000 * 7850 * fond.longueur_pieu_m), "kg", ""))
        items.append(("3.3", "Longrines BA 30x50cm", int(nb_pot * 6), "ml", ""))
    elif fond.beton_semelle_m3 > 0:
        items.append(("3.1", f"Semelles beton arme {rs.classe_beton}",
                       int(fond.beton_semelle_m3 * 0.6), "m3", ""))
        items.append(("3.2", f"Armatures semelles {rs.classe_acier}",
                       int(fond.beton_semelle_m3 * 100), "kg", ""))

    # Structure BA — poteaux
    for i, pt in enumerate(poteaux):
        items.append((f"4.1.{i+1}",
                       f"Poteaux {pt.niveau} {pt.section_mm}x{pt.section_mm}mm {pt.nb_barres}HA{pt.diametre_mm}",
                       nb_pot, "U", f"NEd={pt.NEd_kN:.0f}kN"))

    # Poutres
    b_p = poutre.b_mm / 1000
    h_p = poutre.h_mm / 1000
    L_px = d.nb_travees_x * d.portee_max_m * (d.nb_travees_y + 1) * d.nb_niveaux
    L_py = d.nb_travees_y * d.portee_min_m * (d.nb_travees_x + 1) * d.nb_niveaux
    V_pout = (L_px + L_py) * b_p * (h_p - ep_dalle_m)

    items.append(("4.2", f"Poutres principales {poutre.b_mm}x{poutre.h_mm}mm", int(L_px), "ml", ""))
    items.append(("4.3", f"Poutres secondaires {poutre.b_mm}x{poutre.h_mm}mm", int(L_py), "ml", ""))
    items.append(("4.4", f"Armatures poutres {rs.classe_acier}", int(V_pout * 100), "kg", ""))

    # Dalles
    V_dalles = surf_batie * ep_dalle_m * 0.85
    items.append(("4.5", f"Beton dalles {rs.classe_beton} e={dalle.epaisseur_mm}mm", int(V_dalles), "m3", ""))
    items.append(("4.6", f"Armatures dalles {rs.classe_acier}", int(V_dalles * 80), "kg", ""))
    items.append(("4.7", "Coffrage dalles", int(surf_batie * 0.85), "m2", ""))

    # Escaliers
    nb_esc = max(1, int(surf_batie / 1500))
    items.append(("4.8", f"Escaliers beton arme {rs.classe_beton}", nb_esc * d.nb_niveaux, "volee", f"{nb_esc} cage(s)"))

    # Maconnerie
    surf_facades = int(4 * math.sqrt(d.surface_emprise_m2) * d.nb_niveaux * d.hauteur_etage_m * 0.70)
    items.append(("5.1", "Maconnerie facades agglos pleins 25cm", surf_facades, "m2", ""))
    items.append(("5.2", "Cloisons separatives", int(cl.surface_separative_m2), "m2", ""))
    items.append(("5.3", "Cloisons legeres BA13", int(cl.surface_legere_m2), "m2", ""))
    items.append(("5.4", "Enduits ciment + platre", int((surf_facades + cl.surface_separative_m2) * 2), "m2", ""))

    # Etancheite
    items.append(("6.1", "Etancheite toiture-terrasse SBS", int(d.surface_emprise_m2), "m2", "DTU 43.1"))
    items.append(("6.2", "Etancheite salles de bains", int(surf_batie * 0.08), "m2", ""))

    return items


