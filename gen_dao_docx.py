def generer_dao_docx(rs_structure, rs_mep, params, lot='structure', lang='fr') -> bytes:
    """
    Generate DAO (Dossier d'Appel d'Offres) as Word (.docx).
    BOQ table with quantities only — NO PRICES.

    Args:
        rs_structure: ResultatsStructure object
        rs_mep: ResultatsMEP object
        params: dict of project parameters
        lot: 'structure', 'mep', or 'finitions'
        lang: 'fr' or 'en'

    Returns:
        bytes: DOCX document content
    """
    doc = Document()

    # Set default font
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Calibri'
    font.size = Pt(10)

    # ── COVER ──
    lot_key = _LOT_KEYS.get(lot, "lot_structure")
    lot_label = _t(lot_key, lang)

    title = doc.add_heading(_t("dao_title", lang), level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in title.runs:
        run.font.color.rgb = VERT
        run.font.size = Pt(18)
        run.font.name = 'Calibri'

    subtitle = doc.add_heading(lot_label, level=1)
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph()

    # Project info
    d = rs_structure.params if rs_structure else None
    nom = params.get("nom", getattr(d, "nom", "Projet Tijan") if d else "Projet Tijan")
    ville = params.get("ville", getattr(d, "ville", "Dakar") if d else "Dakar")
    nb_niveaux = params.get("nb_niveaux", getattr(d, "nb_niveaux", 5) if d else 5)
    surface_emprise = params.get("surface_emprise_m2",
                                  getattr(d, "surface_emprise_m2", 500) if d else 500)
    surf_batie = surface_emprise * nb_niveaux

    info_data = [
        [_t("project", lang), nom],
        [_t("location", lang), ville],
        [_t("surface", lang), f'{int(surf_batie):,} m²'.replace(',', ' ')],
        [_t("levels", lang), f'R+{nb_niveaux - 1} ({nb_niveaux})'],
    ]
    _add_styled_table(doc, ['', ''], info_data,
                      col_widths=[5.0, 12.0], header_color='43A956')

    doc.add_page_break()

    # ── BOQ TABLE ──
    doc.add_heading(_t("boq_title", lang), level=1)

    boq_headers = [
        _t("col_no", lang),
        _t("col_desig", lang),
        _t("col_qty", lang),
        _t("col_unit", lang),
        _t("col_obs", lang),
    ]

    if lot == 'structure':
        items = _boq_structure_items(rs_structure, params)
    elif lot == 'mep':
        items = _boq_mep_items(rs_mep, params)
    elif lot == 'finitions':
        items = _boq_finitions_items(rs_structure, params)
    else:
        items = []

    boq_rows = []
    for item in items:
        no, desig, qty, unit, obs = item
        boq_rows.append([
            str(no),
            str(desig),
            str(qty) if qty else '—',
            str(unit),
            str(obs) if obs else '',
        ])

    _add_styled_table(doc, boq_headers, boq_rows,
                      col_widths=[1.5, 7.5, 2.0, 1.5, 4.0],
                      header_color='43A956', zebra=True)

    doc.add_paragraph()

    # ── FOOTER ──
    footer_p = doc.add_paragraph(f'\n\n{_t("generated_by", lang)}')
    footer_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in footer_p.runs:
        run.font.size = Pt(8)
        run.font.italic = True
        run.font.color.rgb = GRIS
        run.font.name = 'Calibri'

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()
