# COST SPLIT RATIOS (fourniture vs pose)
# ══════════════════════════════════════════════════════════════
RATIO_FOURNITURE = {
    "beton":        0.75,
    "acier":        0.60,
    "coffrage":     0.30,
    "terrassement": 0.35,
    "fondations":   0.55,
    "maconnerie":   0.50,
    "etancheite":   0.60,
    "mep":          0.70,
    "finitions":    0.55,
    "installation": 0.40,
    "divers":       0.50,
}


def _ratio_f(cat: str) -> float:
    return RATIO_FOURNITURE.get(cat, 0.50)


def _ratio_p(cat: str) -> float:
    return 1.0 - _ratio_f(cat)


