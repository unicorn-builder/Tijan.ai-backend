def _cover_page(story, rs_structure, params, lot, lang):
    d = rs_structure.params if rs_structure else None
    nom = params.get("nom", getattr(d, "nom", "Projet Tijan") if d else "Projet Tijan")
    ville = params.get("ville", getattr(d, "ville", "Dakar") if d else "Dakar")
    pays = params.get("pays", getattr(d, "pays", "Senegal") if d else "Senegal")
    usage_raw = params.get("usage", getattr(d, "usage", "residentiel") if d else "residentiel")
    usage_val = usage_raw.value if hasattr(usage_raw, "value") else str(usage_raw)
    usage_label = _USAGE_LABELS.get(usage_val, {}).get(lang, usage_val.capitalize())
    nb_niveaux = params.get("nb_niveaux", getattr(d, "nb_niveaux", 5) if d else 5)
    surface = params.get("surface_emprise_m2",
                         getattr(d, "surface_emprise_m2", 500) if d else 500)
    surf_batie = surface * nb_niveaux

    lot_name = _LOT_NAMES.get(lot, _LOT_NAMES["structure"]).get(lang, lot)

    story.append(Spacer(1, 35 * mm))
    story.append(Paragraph(nom, S["titre"]))
    story.append(Spacer(1, 8 * mm))
    story.append(Paragraph(_t("dao_title", lang), S["h1"]))
    story.append(Spacer(1, 4 * mm))
    story.append(Paragraph(lot_name, S["h1"]))
    story.append(Spacer(1, 12 * mm))

    info = [
        [p(f'<b>{_t("location", lang)} :</b> {ville}, {pays}', "body")],
        [p(f'<b>{_t("building_type", lang)} :</b> {usage_label}', "body")],
        [p(f'<b>{_t("surface", lang)} :</b> {fmt_n(surf_batie, 0)} m²'
           f' (emprise {fmt_n(surface, 0)} m²)', "body")],
        [p(f'<b>{_t("levels", lang)} :</b> R+{nb_niveaux - 1}'
           f' ({nb_niveaux} niveaux)', "body")],
        [p(f'<b>{_t("date", lang)} :</b> {datetime.now().strftime("%d/%m/%Y")}',
           "body")],
    ]
    t_info = Table(info, colWidths=[CW])
    t_info.setStyle(TableStyle([
        ("LEFTPADDING",  (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
    ]))
    story.append(t_info)

    story.append(Spacer(1, 20 * mm))
    story.append(Paragraph(_t("generated_by", lang), S["small"]))
    story.append(PageBreak())


