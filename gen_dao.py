# CCTP HELPER — structured article rendering
# ══════════════════════════════════════════════════════════════

def _cctp_article(story, num, titre, sections, lang="fr"):
    """
    Render a CCTP article with sub-sections.
    sections: list of (subtitle_key, text) tuples
    """
    story.append(Spacer(1, 4 * mm))
    story.append(Paragraph(
        f'<b>{_t("article", lang)} {num} — {titre}</b>', S["h2"]))
    story.append(Spacer(1, 1 * mm))
    for subtitle_key, text in sections:
        story.append(Paragraph(
            f'<b>{_t(subtitle_key, lang)}</b>', S["body"]))
        story.append(Paragraph(text, S["body_j"]))
        story.append(Spacer(1, 1 * mm))
    story.append(Spacer(1, 2 * mm))


