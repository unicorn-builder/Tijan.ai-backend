def generer_dao(rs_structure, rs_mep, params: dict,
                lot: str = "structure", lang: str = "fr") -> bytes:
    """
    Generate tender document (DAO) for a specific lot.

    rs_structure: ResultatsStructure from engine_structure_v2 (required for structure/finitions)
    rs_mep: ResultatsMEP from engine_mep_v2 (required for mep)
    params: project params dict
    lot: "structure" | "mep" | "finitions"
    lang: "fr" | "en"

    Returns: PDF bytes
    """
    set_pdf_lang(lang)

    buf = io.BytesIO()

    # Resolve project name for header
    d = rs_structure.params if rs_structure else (rs_mep.params if rs_mep else None)
    nom = params.get("nom", getattr(d, "nom", "Projet Tijan") if d else "Projet Tijan")
    doc_type = _LOT_DOC_TYPE.get(lot, "DAO")

    hf = HeaderFooter(nom, doc_type, lang=lang)
    doc = SimpleDocTemplate(
        buf, pagesize=A4,
        leftMargin=ML, rightMargin=MR,
        topMargin=26 * mm, bottomMargin=18 * mm,
    )

    story = []

    # Cover
    _cover_page(story, rs_structure, params, lot, lang)

    # BOQ + CCTP + Recap per lot
    if lot == "structure":
        if rs_structure is None:
            raise ValueError("rs_structure is required for lot 'structure'")
        lot_recap, total_f, total_p = _boq_structure(
            story, rs_structure, params, lang)
        _cctp_structure(story, rs_structure, params, lang)
        _recap(story, lot_recap, total_f, total_p, lang)

    elif lot == "mep":
        if rs_mep is None:
            raise ValueError("rs_mep is required for lot 'mep'")
        lot_recap, total_f, total_p = _boq_mep(
            story, rs_mep, params, lang)
        _cctp_mep(story, rs_mep, params, lang)
        _recap(story, lot_recap, total_f, total_p, lang)

    elif lot == "finitions":
        lot_recap, total_f, total_p = _boq_finitions(
            story, rs_structure, params, lang)
        _cctp_finitions(story, rs_structure, params, lang)
        _recap(story, lot_recap, total_f, total_p, lang)

    else:
        raise ValueError(f"Unknown lot: {lot}. Use 'structure', 'mep', or 'finitions'.")

    doc.build(story, onFirstPage=hf, onLaterPages=hf)
    return buf.getvalue()
